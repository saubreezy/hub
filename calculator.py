class Calculator:
    def subtract(self, a: float, b: float) -> float:
        """
        Subtracts the second number from the first number.
        
        Args:
            a (float): The first number
            b (float): The number to subtract
            
        Returns:
            float: The result of the subtraction (a - b)
        """
        return a - b

